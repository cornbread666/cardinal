  function getScore(t) {

    t01 = 60; // within 1 minute = 5 stars
    t02 = 150; // within 2.5 minutes = 4 stars
    t03 = 300; // within 5 minutes = 3 stars
    t04 = 600; // within 10 minutes = 2 stars, 10+ minutes = 1 star

    if (t <= t01) {
      stars = 5;
    } else if (t > t01 && t <= t02) {
      stars = 4;
    } else if (t > t02 && t <= t03) {
      stars = 3;
    } else if (t > t03 && t <= t04) {
      stars = 2;
    } else {
      stars = 1;
    }

    return stars;
  }

